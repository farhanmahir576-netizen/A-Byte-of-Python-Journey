# Chapter 5: Operators and Expressions
length = 5
breadth = 2

area = length * breadth
print('Area is', area)
print('Perimeter is', 2 * (length + breadth))

# Logical and shortcut operators
x = 2
x = x + 3 # normal way
print('x is', x)

y = 10
y += 3 # shortcut way (add and assign)
print('y shortcut is', y)

# Boolean evaluation
is_equal = (area == 10)
print('Is area equal to 10?', is_equal)
